    public String[] retornarInformacion(){
        String[] informacion=new String [4];
        
        informacion[0]=this.txt_Cedula.getText();
        informacion[1]=this.txt_Nombre.getText();
        informacion[2]=this.txt_Edad.getText();
        informacion[3]=this.comboBox_Priori.getSelectedItem().toString();
        
       return informacion;
       
    }//fin retornar info
    
